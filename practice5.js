// Write functions to add, subtract, multiply, divide and find remainder of two given numbers. Take 2 number type variables and store their result in another variable.
let mytaka = 56;
let hertaka = 10;
function addmoney(tk1, tk2) {
    let sum = tk1 + tk2;
    return sum;
}
function subtract(tk1, tk2) {
    let subt = tk1 - tk2;
    return subt;
}
function multiply(tk1, tk2) {
    let multi = tk1 * tk2;
    return multi;
}
function remainder(tk1, tk2) {
    let rem = tk1 % tk2;
    return rem;
}
console.log(addmoney(mytaka, hertaka));
console.log(subtract(mytaka, hertaka));
console.log(multiply(mytaka, hertaka));
console.log(remainder(mytaka, hertaka));